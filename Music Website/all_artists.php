function get_all_artists() //function to get all artists
{
    global $connection;
    //creates a new column for age
    $artist_query = 'SELECT DISTINCT ABS(FLOOR(DATEDIFF(date_of_birth, CURRENT_DATE()) / 365)) AS age, stage_name, birth_name, COUNT(songs.song_id) AS song_number, artists.artist_id
                     FROM artists 
                    LEFT JOIN songs ON artists.artist_id = songs.artist_id
                    GROUP BY birth_name';
    
    //determines how to order the data
    if(isset($_GET['sort']) && isset($_GET['by'])){
        $sort = $_GET['sort'];
        $by = $_GET['by'];
        $artist_query .= " ORDER BY $by $sort";
    }
    else{
        $artist_query .= ' ORDER BY birth_name';
    }
    $statement = $connection->prepare($artist_query);
    $statement->execute();
    $artists = $statement->fetchAll(PDO::FETCH_OBJ);
    $statement->closeCursor();
    return $artists;
}

    

