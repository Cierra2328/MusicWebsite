function get_all_albums() //function to gather info about albums
{
    global $connection;
    $album_name_query = 'SELECT album_id, album_name, stage_name, record_label, genre, release_date, artists.artist_id, birth_name
                        FROM albums 
                        JOIN artists ON albums.artist_id = artists.artist_id 
                        JOIN genres on albums.genre_id = genres.genre_id';
    //using the get method to determine how to order the albums
    if(isset($_GET['sort']) && isset($_GET['by'])){
        $sort = $_GET['sort'];
        $by = $_GET['by'];
        $album_name_query .= " ORDER BY $by $sort";
    }
    else{
        $album_name_query .= ' ORDER BY album_name';
    }
    $statement = $connection->prepare($album_name_query); 
    $statement->execute();
    $albums = $statement->fetchAll(PDO::FETCH_OBJ);
    $statement->closeCursor();
    return $albums;
}
